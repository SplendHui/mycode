#include <stdio.h>
int main()
{
	int p1,p2,p3,p4,p5,p6,n;
	long m=0;
	scanf("%d",&n);
	n=n*100;
	//*******************************************************
	for(p1=0;p1<=n/50;p1++)//五角
		for(p2=0;p2<=n/20;p2++)//2角
			for(p3=0;p3<=n/10;p3++)//1角
				for(p4=0;p4<=n/5;p4++)//5分
					for(p5=0;p5<=n/2;p5++)//2fen
						for(p6=0;p6<=n/1;p6++)	//1fen
						{
							if( (p6+2*p5+p4*5+p3*10+p2*20+p1*50)==n)
							m++;
							
						}
	//********************************************************
	printf("%ld \n",m);
	return 0;
}
